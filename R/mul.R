#' @title Multiply Two Numbers
#'
#' @description
#' This function multiplies two numeric inputs, \code{i} and \code{j}.
#'
#' @param i A numeric value representing the first number.
#' @param j A numeric value representing the second number.
#'
#' @return A numeric value equal to \code{i * j}.
#'
#' @export
mul <- function(i, j) {
  return(i * j)
}
